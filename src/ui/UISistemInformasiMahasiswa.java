    private void jTableNilaiMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableNilaiMouseClicked
        int row = jTableNilai.getSelectedRow();

        if (row >= 0) {
            // Ambil ID dari kolom 0 (yang di-hidden tadi)
            selectedIdNilai = Integer.parseInt(jTableNilai.getValueAt(row, 0).toString());

            // NIM & Nama (Index 2 & 3)
            String nimNama = jTableNilai.getValueAt(row, 2).toString() + " - " + jTableNilai.getValueAt(row, 3).toString();
            jComboBoxNamadiNilai.setSelectedItem(nimNama);

            // Kode & Matkul (Index 4 & 5)
            String kodeMatkul = jTableNilai.getValueAt(row, 4).toString() + " - " + jTableNilai.getValueAt(row, 5).toString();
            jComboBoxMatkul.setSelectedItem(kodeMatkul);

            // Nilai (Index 6)
            jTextFieldNilai.setText(jTableNilai.getValueAt(row, 6).toString());

            // Nilai Huruf (Index 7)
            jTextFieldNilaiHuruf.setText(jTableNilai.getValueAt(row, 7).toString());
        }
    }//GEN-LAST:event_jTableNilaiMouseClicked

