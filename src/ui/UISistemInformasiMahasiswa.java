private void loadMahasiswaData() {
    DefaultTableModel model = (DefaultTableModel) jTableMahasiswa.getModel();
    model.setRowCount(0);

    mahasiswaController.getAll().forEach(m -> {
        model.addRow(new Object[]{
            m.getNim(),
            m.getNama(),
            m.getJurusan(),
            m.getJenisKelamin(),
            m.getAlamat()
        });
    });
}
private void loadMatkulData() {
    DefaultTableModel model = (DefaultTableModel) jTableMatkul.getModel();
    model.setRowCount(0);

    mataKuliahController.getAll().forEach(mk -> {
        model.addRow(new Object[]{
            mk.getKodeMk(),
            mk.getNamaMk(),
            mk.getSks()
        });
    });
}
private void loadNilaiData() {
    DefaultTableModel model = (DefaultTableModel) jTableNilai.getModel();
    model.setRowCount(0);

    int no = 1;
    for (Nilai n : nilaiController.getAll()) {
        model.addRow(new Object[]{
            n.getIdNilai(),      
            no++,                
            n.getNim(),          
            n.getNamaMahasiswa(),
            n.getKodeMk(),       
            n.getNamaMatkul(),   
            n.getNilai(),        
            n.getNilaiHuruf()    
        });
    }

   
    jTableNilai.getColumnModel().getColumn(0).setMinWidth(0);
    jTableNilai.getColumnModel().getColumn(0).setMaxWidth(0);
    jTableNilai.getColumnModel().getColumn(0).setWidth(0);
    jTableNilai.getColumnModel().getColumn(0).setPreferredWidth(0);
}
    
private void loadComboMahasiswa() {
    jComboBoxNamadiNilai.removeAllItems();

    mahasiswaController.getAll().forEach(m -> {
        jComboBoxNamadiNilai.addItem(m.getNim() + " - " + m.getNama());
    });
}
private void loadComboMatkul() {
    jComboBoxMatkul.removeAllItems();

    mataKuliahController.getAll().forEach(mk -> {
        jComboBoxMatkul.addItem (mk.getKodeMk()+ " - " + mk.getNamaMk());
    });
}
