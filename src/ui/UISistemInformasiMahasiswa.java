    private void jTextFieldNilaiKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextFieldNilaiKeyReleased
        try {
            String input = jTextFieldNilai.getText().trim();
            if (input.isEmpty()) {
                jTextFieldNilaiHuruf.setText("");
                return;
            }

            int nilaiAngka = Integer.parseInt(input);

            // Panggil method controller yang sudah diubah jadi public
            String huruf = nilaiController.hitungNilaiHuruf(nilaiAngka);

            jTextFieldNilaiHuruf.setText(huruf);
        } catch (NumberFormatException e) {
            jTextFieldNilaiHuruf.setText("");
        }
    }//GEN-LAST:event_jTextFieldNilaiKeyReleased

