    private void jButtonHapusMahasiswaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonHapusMahasiswaActionPerformed
        int row = jTableMahasiswa.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Pilih data di tabel dulu");
            return;
        }

        String nim = jTableMahasiswa.getValueAt(row, 0).toString();

        int confirm = JOptionPane.showConfirmDialog(
            this,
            "Yakin hapus mahasiswa dengan NIM " + nim + "?",
            "Konfirmasi",
            JOptionPane.YES_NO_OPTION
        );

        if (confirm != JOptionPane.YES_OPTION) return;

        try {
            mahasiswaController.hapus(nim);

            JOptionPane.showMessageDialog(this, "Data berhasil dihapus");
            loadMahasiswaData();
            clearMahasiswaForm();

        } catch (Exception e) {
            JOptionPane.showMessageDialog(
                this,
                e.getMessage(),
                "Terjadi Kesalahan",
                JOptionPane.ERROR_MESSAGE
            );
        }
    }//GEN-LAST:event_jButtonHapusMahasiswaActionPerformed

