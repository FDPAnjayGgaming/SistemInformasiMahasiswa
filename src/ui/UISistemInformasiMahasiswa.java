    private void jButtonHapusMatkulActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonHapusMatkulActionPerformed
        int row = jTableMatkul.getSelectedRow();
        if (row < 0) {
            JOptionPane.showMessageDialog(this, "Pilih data di tabel");
            return;
        }

        String kode = jTableMatkul.getValueAt(row, 0).toString();

        if (JOptionPane.showConfirmDialog(
            this, "Hapus mata kuliah " + kode + "?",
            "Konfirmasi", JOptionPane.YES_NO_OPTION
        ) == JOptionPane.YES_OPTION) {

            mataKuliahController.hapus(kode);
            loadMatkulData();
            clearMatkulForm();
        }
    }//GEN-LAST:event_jButtonHapusMatkulActionPerformed

    private void jButtonTambahMatkulActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonTambahMatkulActionPerformed
        try {
            String kode = jTextFieldKodeMatkul.getText().trim();
            String nama = jTextFieldNamaMatkul.getText().trim();
            String sksStr = jTextFieldSKS.getText().trim();

            if (kode.isEmpty() || nama.isEmpty() || sksStr.isEmpty()) {
                JOptionPane.showMessageDialog(this, "Semua field wajib diisi");
                return;
            }

            int sks = Integer.parseInt(sksStr);
            mataKuliahController.simpan(kode, nama, sks);

            JOptionPane.showMessageDialog(this, "Data mata kuliah berhasil ditambahkan");
            loadMatkulData();
            loadComboMatkul();
            clearMatkulForm();
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "SKS harus berupa angka");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Gagal tambah data: " + e.getMessage());
        }
    }//GEN-LAST:event_jButtonTambahMatkulActionPerformed

